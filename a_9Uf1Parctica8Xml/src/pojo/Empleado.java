package pojo;

public class Empleado {
	
	private int id;
	private String nombre;
	private String apellido;
	
	public Empleado(int id, String nombre, String apellido) {
		super();
		this.id = id;
		this.nombre = nombre;
		this.apellido = apellido;
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}

	public String getApellido() {
		return apellido;
	}
	
	
	
	


}
