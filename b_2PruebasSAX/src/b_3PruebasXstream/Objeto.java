package b_3PruebasXstream;

public class Objeto {

	
	private String nombre;
	private int valor;
	
	public Objeto(String nombre, int valor) {
		super();
		this.nombre = nombre;
		this.valor = valor;
	}

	public String getNombre() {
		return nombre;
	}

	public int getValor() {
		return valor;
	}
	
	
	
	

}
